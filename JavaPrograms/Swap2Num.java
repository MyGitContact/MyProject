public class Swap2Num
{
public static void main(String[] args)
{
int a=10;
int b=20;
int temp;

System.out.println("Values before Swapping are : ");	
System.out.println("a = "+a+" "+"b = "+b);				

temp=a;
a=b;
b=temp;


System.out.println("Values After Swapping are : ");	
System.out.println("a = "+a+" "+"b = "+b);				

}

}